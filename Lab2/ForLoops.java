package week2;

public class ForLoops {
    public static void main(String[] args) {



        for (int x = 0 ; x < 5 ; x++ ) {
            System.out.println(x+1);
        }

        for (int x=10; x<=0; x--){
            System.out.println(x + "...");
        }
        System.out.println("Rocket Launch!!");
    }
}
